public class BaseController {

    @ExceptionHandler(Exception.class)
    @ResponseStatus(HttpStatus.OK)
    @ResponseBody
    public Object handlerException(HttpServletRequest request, HttpServletResponse response, Exception e) {
        Map<String, Object> map = new HashMap<>();
        if (e instanceof MyError) {
            MyError myError = (MyError) e;
            map.put("code", myError.getErrorCode());
            map.put("msg", myError.getErrorMsg());
        } else {
            map.put("code", BaseError.UNKNOWN_ERROR.getErrorCode());
            map.put("msg", BaseError.UNKNOWN_ERROR.getErrorMsg());
        }
        return Result.create(map);
    }

}